class PrintingTable {
	public static void main(String[] args) {
		System.out.println("a \tb \tpow(a, b)" );
		System.out.println("1 \t2 \t" + Math.pow(1, 2));
		System.out.println("2 \t3 \t" + Math.pow(2, 3));
		System.out.println("3 \t4 \t" + Math.pow(3, 4));
		System.out.println("4 \t5 \t" + Math.pow(4, 5));
		System.out.println("5 \t6 \t" + Math.pow(5, 6));
		
	}
}